                <h4>What is the IMEI Number? </h4>
                <p>IMEI Number stands for the International Mobile Equipment Identity this number is unique to every mobile phone. The IMEI number can be found by pressing *#06# on your mobile phone. If it cannot be found in this way, the IMEI is usually printed on a label which is in the battery compartment of the mobile phone. Certain devices can also have the IMEI number printed on the back or on the sim card tray.  </p>
